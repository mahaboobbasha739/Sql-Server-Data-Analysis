							-- CUBE CLAUSE --

-- IT IS USED TO FIND SUB AND GRAND TOTAL OF GRAND TOTAL OF A MULTIPLE COLUMN.

SELECT JOB,DNO,COUNT(*) AS TOTALJOBS FROM EMP GROUP BY CUBE(DNO,JOB)



